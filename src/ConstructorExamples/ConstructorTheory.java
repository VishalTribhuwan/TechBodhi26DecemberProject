// Constructor:- 

// When Class Name & Method Name is same, then its called as Constructor. 
// Constructor is used to initilize an object (not to create object).
// It is a block (similar to method) having same as that of class name.
// Does not have any return type, not even void.
// The only modifiers applicable for constructor are public, protected, default & private.
// It executes automatically when we create an object.

// Types of Constructor:- 
// 1) Default Constructor - Created by Compiler
// 2) No Argument Constructor (User Defined) - Created by Programmer
