package ConditionalStatements;

public class ForEachExample {     // It also called as Advance for loop

	public static void main(String[] args) {
		
		int arr [] = {20,23,85,1,6,7,29};	// stored collection (Multiple) values in array 
		
		for(int vishal:arr) {  // fetching the each & every value of collection, we used for loop                
			
		System.out.println(vishal);
		
		}

	}

}
